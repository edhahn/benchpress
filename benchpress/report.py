class BenchpressReport(FPDF):
    def __init__(self) -> None:
        super().__init__(orientation="L", format="A4")
        self.set_auto_page_break(auto=True, margin=20)

    def header(self) -> None:
        if self.page_no() > 1:
            self.set_font("Helvetica", "I", 8)
            self.set_text_color(*COLOR_HEADER)
            self.cell(0, 6, "Benchpress Report", align="L")
            self.cell(0, 6, f"Page {self.page_no()}", align="R")
            self.ln(10)

    def footer(self) -> None:
        self.set_y(-15)
        self.set_font("Helvetica", "I", 7)
        self.set_text_color(*COLOR_MUTED)
        self.cell(0, 10, "Generated by Benchpress", align="C")


# ---------------------------------------------------------------------------
# Public entry point
# ---------------------------------------------------------------------------

async def generate_report(
    run: BenchmarkRun,
    config: BenchpressConfig,
    output_path: str,
) -> str:
    pdf = BenchpressReport()
    summaries = run.summaries

    # Build comparison groups from base_model tags
    comparisons = _build_comparisons(config, summaries)

    # Generate LLM insights (best-effort)
    insights = await _generate_insights(run, config, comparisons)

    # Render pages
    _title_page(pdf, run, insights.get("executive_summary"))
    _head_to_head_pages(pdf, comparisons, insights)
    _unmatched_page(pdf, comparisons, summaries)
    _appendix_tables(pdf, summaries, run)

    Path(output_path).parent.mkdir(parents=True, exist_ok=True)
    pdf.output(output_path)
    return output_path


# ---------------------------------------------------------------------------
# Comparison grouping
# ---------------------------------------------------------------------------

class ComparisonGroup:
    def __init__(self, base_model: str, display_label: str):
        self.base_model = base_model
        self.display_label = display_label
        self.entries: list[tuple[ProviderConfig, ModelConfig, ModelSummary]] = []


def _build_comparisons(
    config: BenchpressConfig,
    summaries: list[ModelSummary],
) -> list[ComparisonGroup]:
    # Map (provider, model_id) -> ModelSummary
    summary_lookup: dict[tuple[str, str], ModelSummary] = {
        (s.provider, s.model): s for s in summaries
    }

    # Map (provider, model_id) -> (ProviderConfig, ModelConfig)
    config_lookup: dict[tuple[str, str], tuple[ProviderConfig, ModelConfig]] = {}
    for p in config.providers:
        for m in p.models:
            config_lookup[(p.name, m.id)] = (p, m)

    # Group by base_model
    groups: dict[str, ComparisonGroup] = {}
    for p in config.providers:
        for m in p.models:
            if not m.base_model:
                continue
            key = (p.name, m.id)
            summary = summary_lookup.get(key)
            if not summary:
                continue
            if m.base_model not in groups:
                groups[m.base_model] = ComparisonGroup(
                    base_model=m.base_model,
                    display_label=m.base_model.replace("-", " ").title(),
                )
            groups[m.base_model].entries.append((p, m, summary))

    # Only return groups with 2+ entries (actual comparisons)
    return [g for g in groups.values() if len(g.entries) >= 2]


def _get_unmatched(
    comparisons: list[ComparisonGroup],
    summaries: list[ModelSummary],
) -> list[ModelSummary]:
    matched = set()
    for g in comparisons:
        for _, _, s in g.entries:
            matched.add((s.provider, s.model))
    return [s for s in summaries if (s.provider, s.model) not in matched]


# ---------------------------------------------------------------------------
# LLM Insight Generation
# ---------------------------------------------------------------------------

async def _generate_insights(
    run: BenchmarkRun,
    config: BenchpressConfig,
    comparisons: list[ComparisonGroup],
) -> dict[str, str]:
    """Generate LLM insights. Returns dict with 'executive_summary' and per-base_model keys."""
    insights: dict[str, str] = {}

    llm_config = _resolve_llm_config(config)
    if not llm_config:
        return insights

    base_url, model, bearer_token = llm_config

    try:
        insights["executive_summary"] = await _call_llm(
            base_url, model, bearer_token,
            _build_executive_prompt(run, config, comparisons),
        )
    except Exception as e:
        logger.warning(f"Failed to generate executive summary insight: {e}")

    for group in comparisons:
        try:
            insights[group.base_model] = await _call_llm(
                base_url, model, bearer_token,
                _build_comparison_prompt(group, config),
            )
        except Exception as e:
            logger.warning(f"Failed to generate insight for {group.base_model}: {e}")

    return insights


def _resolve_llm_config(
    config: BenchpressConfig,
) -> tuple[str, str, str | None] | None:
    """Returns (base_url, model, bearer_token) or None if not configured."""
    report = config.report

    if report.llm:
        return (
            report.llm.base_url,
            report.llm.model,
            report.llm.resolve_api_key(),
        )

    if report.llm_provider and report.llm_model:
        for p in config.providers:
            if p.name == report.llm_provider:
                return (
                    p.base_url,
                    report.llm_model,
                    p.resolve_api_key(),
                )
        logger.warning(
            f"Report llm_provider '{report.llm_provider}' not found in providers"
        )

    return None


async def _call_llm(
    base_url: str,
    model: str,
    bearer_token: str | None,
    prompt: str,
) -> str:
    headers: dict[str, str] = {"Content-Type": "application/json"}
    if bearer_token:
        headers["Authorization"] = f"Bearer {bearer_token}"

    payload = {
        "model": model,
        "messages": [{"role": "user", "content": prompt}],
        "max_tokens": 1024,
        "temperature": 0.3,
    }

    async with httpx.AsyncClient(
        base_url=base_url,
        headers=headers,
        timeout=httpx.Timeout(60.0, connect=15.0),
    ) as client:
        resp = await client.post("/chat/completions", json=payload)
        resp.raise_for_status()
        data = resp.json()
        return data["choices"][0]["message"]["content"]


def _build_executive_prompt(
    run: BenchmarkRun,
    config: BenchpressConfig,
    comparisons: list[ComparisonGroup],
) -> str:
    provider_contexts = []
    for p in config.providers:
        ctx = f"- {p.name}"
        if p.insights_context:
            ctx += f": {p.insights_context.strip()}"
        provider_contexts.append(ctx)

    summary_lines = []
    for s in run.summaries:
        summary_lines.append(
            f"  {s.display_name} ({s.provider}): "
            f"success={s.success_rate:.0f}%, "
            f"avg_ms={s.avg_total_ms:.0f}, "
            f"p95_ms={s.p95_total_ms:.0f}, "
            f"tool_acc={s.avg_tool_accuracy:.1%}, "
            f"coherence={s.avg_coherence:.1%}, "
            f"cost=${s.total_cost_usd:.4f}"
        )

    comparison_lines = []
    for g in comparisons:
        entries = ", ".join(
            f"{m.display_name} ({p.name})" for p, m, _ in g.entries
        )
        comparison_lines.append(f"  {g.display_label}: {entries}")

    return f"""You are writing the executive summary for a benchmark report comparing LLM providers.
Write 2-3 concise paragraphs summarizing the key findings. Be balanced and constructive.

Provider context:
{chr(10).join(provider_contexts)}

Benchmark results:
{chr(10).join(summary_lines)}

Head-to-head comparisons:
{chr(10).join(comparison_lines)}

Configuration: {run.config_summary}

Guidelines:
- Lead with the most important finding
- Compare providers constructively, acknowledging each provider's value proposition
- Mention specific numbers (latency, accuracy, cost) to support claims
- Keep it professional and suitable for stakeholder presentations
- If all requests failed for a provider, note this factually and suggest possible causes
- Do not use markdown formatting, bullet points, or headers — write flowing prose paragraphs"""


def _build_comparison_prompt(
    group: ComparisonGroup,
    config: BenchpressConfig,
) -> str:
    entries_text = []
    for p, m, s in group.entries:
        ctx = ""
        if p.insights_context:
            ctx = f"\n    Provider context: {p.insights_context.strip()}"
        entries_text.append(
            f"  {m.display_name} ({p.name}):{ctx}\n"
            f"    Success: {s.success_rate:.0f}% ({s.success_count}/{s.total_requests})\n"
            f"    Latency: avg={s.avg_total_ms:.0f}ms, p50={s.p50_total_ms:.0f}ms, p95={s.p95_total_ms:.0f}ms\n"
            f"    TTFB: avg={s.avg_ttfb_ms:.0f}ms, p95={s.p95_ttfb_ms:.0f}ms\n"
            f"    Quality: tool_acc={s.avg_tool_accuracy:.1%}, coherence={s.avg_coherence:.1%}, relevance={s.avg_relevance:.1%}\n"
            f"    Cost: total=${s.total_cost_usd:.4f}, per_request=${s.avg_cost_per_request:.5f}\n"
            f"    Tokens: total_input={s.total_input_tokens:,}, total_output={s.total_output_tokens:,}, "
            f"avg_input={s.avg_input_tokens:.0f}, avg_output={s.avg_output_tokens:.0f}"
        )

    return f"""You are writing a comparison insight for the base model "{group.display_label}" deployed across different providers.
Write 1-2 concise paragraphs comparing these deployments. Be constructive and balanced.

Deployments:
{chr(10).join(entries_text)}

Guidelines:
- Highlight meaningful differences in latency, quality, and cost
- Use delta language: "X is Nms faster" or "Y costs Z% less"
- Acknowledge each provider's value-add per the provider context
- If one provider had failures, note it factually without harsh judgment
- Keep it professional and suitable for stakeholder presentations
- Do not use markdown formatting, bullet points, or headers — write flowing prose paragraphs"""


